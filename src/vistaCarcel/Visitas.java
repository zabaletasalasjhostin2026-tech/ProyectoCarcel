    private void btnVerificarVisitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarVisitaActionPerformed
        // TODO add your handling code here:
        String ciVisitante = txtCiVisitanteVerificacion.getText();
        String ciRecluso = txtCiReclusoVerificacion.getText();
        int swv = 0, swr = 0;

        for (Visita v : ControladorSistema.operaciones.getListaVisita()) {
            if (v.getCi().equalsIgnoreCase(ciVisitante)) {
                swv = 1;
                for (Recluso r : ControladorSistema.operaciones.getListaRecluso()) {
                    if (r.getNombre().equalsIgnoreCase(v.getRecluso()) && r.getCi().equalsIgnoreCase(ciRecluso)) {
                        swr = 1;
                        v.setVerificacion("APROBADO");
                        msjLabel.setText("Verificación exitosa, puede realizar la visita.");
                        cargarTablaReclusos(ControladorSistema.operaciones.getListaVisita());

                    }
                }
                if (swr == 0) {
                    v.setVerificacion("RECHAZADO");
                    msjLabel.setText("Recluso no encontrado, verificacion rechazada");
                }

            }
        }
        if (swv == 0) {
            msjLabel.setText("CI. de visitante no encontrado");

        }

    }//GEN-LAST:event_btnVerificarVisitaActionPerformed

