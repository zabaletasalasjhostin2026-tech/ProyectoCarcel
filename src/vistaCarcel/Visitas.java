    private void btnBuscarPorFechaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPorFechaActionPerformed
        // TODO add your handling code here:
        String fechaBuscada = txtFechaBuscar.getText();
        int sw = 0;
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nombre");
        modelo.addColumn("Edad");
        modelo.addColumn("CI");
        modelo.addColumn("Parentesco");
        modelo.addColumn("Fecha");

        modelo.addColumn("Fecha Salida");
        modelo.addColumn("Recluso");
        modelo.addColumn("Verificación");
        modelo.addColumn("Estado");

        for (Visita v : ControladorSistema.operaciones.getListaVisita()) {
            if (v.getFecha().equalsIgnoreCase(fechaBuscada)) {
                sw = 1;
                Object[] fila = {
                    v.getNombre(), v.getEdad(), v.getCi(), v.getParentesco(),
                    v.getFecha(), v.getFechaSa(), v.getRecluso(), v.getVerificacion(),v.getEstado()
                };
                modelo.addRow(fila);
            }
        }
        if (sw == 0) {
            msjLabel.setText("No se encontro visitas para esa fecha");
        } else {
            tablaVisitas.setModel(modelo);
            msjLabel.setText("");
        }

    }//GEN-LAST:event_btnBuscarPorFechaActionPerformed

