    private void confirmarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarBtnActionPerformed
        // TODO add your handling code here:
        String ci = txtVisi.getText();
        int sw = 0;

        for (Visita v : ControladorSistema.operaciones.getListaVisita()) {

            if (v.getCi().equalsIgnoreCase(ci)) {
                sw = 1;

                if (v.getEstado().equalsIgnoreCase("DENTRO")) {

                    String fechaActual = LocalDate.now()
                            .format(DateTimeFormatter.ofPattern("dd/MM/yyyy"));

                    v.setFechaSa(fechaActual);
                    v.setEstado("SALIO");

                    msjLabel.setText("Salida registrada correctamente");
                    cargarTablaReclusos(ControladorSistema.operaciones.getListaVisita());

                } else {
                    msjLabel.setText("El visitante no se encuentra dentro");
                }

                break;
            }
        }

        if (sw == 0) {
            msjLabel.setText("Visitante no encontrado");
        }
    }//GEN-LAST:event_confirmarBtnActionPerformed

