    private void realizarVisitaOsiBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_realizarVisitaOsiBtnActionPerformed
        // TODO add your handling code here:
        //este metodo realiza la visita pero preguntando si el visitante hizo o no hizo la verificacio
        int sw = 0;
        String ciVisitante = txtCiVisitar.getText();
        for (Visita v : ControladorSistema.operaciones.getListaVisita()) {
            if (v.getCi().equalsIgnoreCase(ciVisitante)) {
                sw = 1;
                if (v.getVerificacion().equalsIgnoreCase("APROBADO")) {
                    JOptionPane.showMessageDialog(this,
                            "Visita realizada con exito!\n"
                            + "Visitante: " + v.getNombre() + "\n"
                            + "Recluso: " + v.getRecluso() + "\n"
                            + "Parentesco: " + v.getParentesco() + "\n"
                            + "Fecha: " + v.getFecha());
                    //una vez que el visitante realizo la visita, se lo elimina de la lista
                    v.setEstado("DENTRO");
                    break;
                } else {
                    if (v.getVerificacion().equalsIgnoreCase("PENDIENTE")) {
                        msjLabel.setText("PORFAVOR REALICE LA VERIFICACION!");
                    } else {
                        JOptionPane.showMessageDialog(this,
                                "La visita fue rechazada.\nDebe registrarse nuevamente si desea volver a intentarlo.");
                        ControladorSistema.operaciones.getListaVisita().remove(v);
                        break;
                    }
                }
            }
        }
        if (sw == 0) {
            msjLabel.setText("Usted no se encuentra registrado en el sistema");
        }

    }//GEN-LAST:event_realizarVisitaOsiBtnActionPerformed

