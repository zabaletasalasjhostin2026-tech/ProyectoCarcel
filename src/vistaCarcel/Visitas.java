    //metodo para cargar la tabla y que guarde cada dato en cada columna y fila
    private void cargarTablaReclusos(List<Visita> lista) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nombre");
        modelo.addColumn("Edad");
        modelo.addColumn("CI");
        modelo.addColumn("Parentesco");
        modelo.addColumn("Fecha");

        modelo.addColumn("Fecha Salida");
        modelo.addColumn("Recluso");
        modelo.addColumn("Verificación");
        modelo.addColumn("Estado");

        for (Visita a : lista) {
            modelo.addRow(new Object[]{
                a.getNombre(),
                a.getEdad(),
                a.getCi(),
                a.getParentesco(),
                a.getFecha(),
                a.getFechaSa(),
                a.getRecluso(),
                a.getVerificacion(),
                a.getEstado()});
        }
        //pasamos los datos a la tabla, seteando :D
        tablaVisitas.setModel(modelo);
    }

