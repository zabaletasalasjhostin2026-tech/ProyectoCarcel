    private void btnConfirmarTurnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarTurnoActionPerformed
        // TODO add your handling code here:
        String ci = txtCiAdmin.getText();
        String turnoActual = txtTurnoActual.getText();
        String nuevoTurno = txtNuevoTurno.getText();

        int sw = 0;

        for (Administrativo a : ControladorSistema.operaciones.getListaAdmin()) {

            if (a.getCi().equalsIgnoreCase(ci)) {

                sw = 1;

                a.setTurno(nuevoTurno);

                msjLabel.setText("Turno cambiado correctamente");
                cargarTablaReclusos(
                        ControladorSistema.operaciones.getListaAdmin()
                );
                break;
            }
        }

        if (sw == 0) {
            msjLabel.setText("No se encontró el CI");
        }

        
    }//GEN-LAST:event_btnConfirmarTurnoActionPerformed

