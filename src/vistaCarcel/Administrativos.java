    private void btnPagarSueldoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarSueldoActionPerformed
        // TODO add your handling code here:
        String ci = txtCiPagar.getText();
        int sw = 0;
        double bono = 0;
        msjLabel.setText("");

        for (Administrativo a : ControladorSistema.operaciones.getListaAdmin()) {
            if (a.getCi().equalsIgnoreCase(ci)) {
                sw = 1;

                double total = a.getSueldo();

                if (a.getTurno().equalsIgnoreCase("noche")) {
                    //se da un bono del 20% por turnos noche
                    bono = a.getSueldo() * 0.20;
                    total = total + bono;
                }
                if (bono > 0) {
                    JOptionPane.showMessageDialog(this,
                            "Administrativo: " + a.getNombre() + "\n"
                            + "Sueldo base: " + a.getSueldo() + "\n"
                            + "Bono: " + bono + " por trabajo noche \n"
                            + "Sueldo total: " + total);
                    break;
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Administrativo: " + a.getNombre() + "\n"
                            + "Sueldo base: " + a.getSueldo() + "\n"
                            + "Bono: Sin Bono Extra \n"
                            + "Sueldo total: " + a.getSueldo());
                    break;
                }

            }
        }
        if (sw == 0) {
            msjLabel.setText("No se Econtro Nro. de CI");
        }


    }//GEN-LAST:event_btnPagarSueldoActionPerformed

    private void btnDespedirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDespedirActionPerformed
        // TODO add your handling code here:
        String ci = txtCiDespedir.getText();
        int sw = 0;
        msjLabel.setText("");

        for (Administrativo a : ControladorSistema.operaciones.getListaAdmin()) {
            if (a.getCi().equalsIgnoreCase(ci)) {
                sw = 1;
                int confirm = JOptionPane.showConfirmDialog(this,
                        "¿Está seguro de despedir a " + a.getNombre() + "?",
                        "Confirmar", JOptionPane.YES_NO_OPTION);

                if (confirm == JOptionPane.YES_OPTION) {
                    ControladorSistema.operaciones.getListaAdmin().remove(a);
                    msjLabel.setText("Administrativo despedido");
                    mostrarTodosBtnActionPerformed(null); // refrescar tabla
                } else {
                    msjLabel.setText("Operacion Cancelada");
                }
                break;
            }
        }
        if (sw == 0) {
            msjLabel.setText("No se encontro Nro. de Ci");
        }

    }//GEN-LAST:event_btnDespedirActionPerformed

