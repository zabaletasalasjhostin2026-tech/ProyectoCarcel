    private void CambiarCeldaBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CambiarCeldaBtnActionPerformed
        // TODO add your handling code here:
        String celdaActual = txtCeldaActual.getText();
        String nuevaCelda = txtNuevaCelda.getText();

        int sw = 0;

        for (Recluso r : ControladorSistema.operaciones.getListaRecluso()) {

            if (r.getNroCelda().equals(celdaActual)) {

                sw = 1;
                r.setNroCelda(nuevaCelda);

                msjLable.setText("Celda cambiada correctamente");
                cargarTablaReclusos(
                        ControladorSistema.operaciones.getListaRecluso()
                );
                break;
            }
        }

        if (sw == 0) {
            msjLable.setText("Celda no encontrada");
        }
    }//GEN-LAST:event_CambiarCeldaBtnActionPerformed

