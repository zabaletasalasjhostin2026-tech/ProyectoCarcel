    //metodo para crear la tabla
    private void cargarTablaReclusos(List<Recluso> lista) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nombre");
        modelo.addColumn("Edad");
        modelo.addColumn("CI.");
        modelo.addColumn("Delito");
        modelo.addColumn("Condena");
        modelo.addColumn("Nro. Celda");
        //aca en este forsito recoge todos lo datos y lo lleva a la tabla 
        for (Recluso r : lista) {
            modelo.addRow(new Object[]{
                r.getNombre(),
                r.getEdad(),
                r.getCi(),
                r.getDelito(),
                r.getCondena(),
                r.getNroCelda()
            });
        }
        tablaReclusos.setModel(modelo);
    }

