    private void btnBuscarCeldaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCeldaActionPerformed
        // TODO add your handling code here:
        int sw = 0;
        String celdaBuscada = txtBuscarCelda.getText();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nombre");
        modelo.addColumn("Edad");
        modelo.addColumn("CI.");
        modelo.addColumn("Delito");
        modelo.addColumn("Condena");
        modelo.addColumn("Nro. Celda");

        for (Recluso r : ControladorSistema.operaciones.getListaRecluso()) {
            if (r.getNroCelda().equalsIgnoreCase(celdaBuscada)) {
                sw = 1;
                Object[] fila = {
                    r.getNombre(), r.getEdad(), r.getCi(),
                    r.getDelito(), r.getCondena(), r.getNroCelda()
                };
                modelo.addRow(fila);
            }
        }

        if (sw == 0) {
            msjLable.setText("No se encontro ningun recluso");
        } else {
            tablaReclusos.setModel(modelo);
            msjLable.setText("");
        }

    }//GEN-LAST:event_btnBuscarCeldaActionPerformed

